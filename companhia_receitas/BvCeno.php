<section id="rece">
  <h1 class="text3">
  	<br>
  	Ingredientes:
  	<br>
  	<br>
  	• 2 cenouras grandes;
  	<br>
  	• 1/2 de xícara de chá de óleo vegetal;
  	<br>
  	• 1 ½ xícara de chá de água;
  	<br>
  	• 3 xícaras de chá de farinha de trigo integral;
  	<br>
    • 2 xícaras de chá de açúcar de coco;
    <br>
    • 2 colheres de sobremesa de fermento em pó;
    <br>
    • 1 pitada de sal.
    <br>
  	<br>
  	Unte uma forma e polvilhe com farinha. Pré-aqueça o forno a 190ºC. Em um bowl, misture a farinha, o açúcar e o sal. Pique a cenoura descascada e leve ao liquidificador com a água e o óleo e bata bem até obter uma mistura homogênea. Adicione aos poucos a mistura líquida à mistura seca, mexendo sempre até formar uma massa. Adicione o fermento por último e misture bem sem bater. Despeje a massa na forma e leve ao forno por 45 minutos ou até dourar. Deixe esfriar antes de desenformar e servir.
  </h1>
  